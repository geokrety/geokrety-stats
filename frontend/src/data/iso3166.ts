/**
 * Returns the alpha-2 country code for a given numeric ISO code.
 * @param numericId - Numeric ISO 3166-1 code (may be padded or not)
 */
export function numericToAlpha2(numericId: string | number): string | undefined {
  const padded = String(numericId).padStart(3, '0')
  return NUMERIC_TO_ALPHA2[padded]
}
